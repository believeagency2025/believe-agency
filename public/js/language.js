function toggleLanguage() {
    const currentLang = localStorage.getItem('lang') || 'en';
    const newLang = currentLang === 'en' ? 'ar' : 'en';
    setLanguage(newLang);
}

function setLanguage(lang) {
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    console.log("Setting language to:", lang);

    if (lang === 'ar') {
        document.documentElement.classList.add('rtl');
    } else {
        document.documentElement.classList.remove('rtl');
    }

    // Update text content
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        // console.log("Translating:", key, "Found:", translations[lang][key]);
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Update toggle buttons
    const btnText = lang === 'ar' ? 'En' : 'ع';
    const mobileBtnText = lang === 'ar' ? 'English' : 'العربية';

    const desktopBtn = document.getElementById('lang-toggle');
    if (desktopBtn) desktopBtn.textContent = btnText;

    const mobileBtn = document.getElementById('lang-toggle-mobile');
    if (mobileBtn) mobileBtn.textContent = mobileBtnText;
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('lang') || 'en';
    setLanguage(savedLang);
});
